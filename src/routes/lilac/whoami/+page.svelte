        <h2>ꕤ who am i, compared to sydney?</h2>
        <div class="horizontal-divide"></div>
        <p>if you’re already familiar with DID - you’ll probably understand right away who i am, and what my relationship with sydney is like. i’m one of her alters, but front quite a bit and have a large presence in her life. if terms like “front” or “alter” are unfamiliar to you, then don’t worry~! you’ll soon have a much better understanding of what DID is, and importantly to us, how it shows up in our lives. for the completely uninitiated, here’s a simple overview from wikipedia that deals more with the direct consequences in one’s life. (i won’t get into the complicated history or controversies around the disorder, that’s not for here~)</p>
        <div id="did-definition">
            <img id="sya-my-beloved" src="https://media1.tenor.com/m/B8PaZ6UGHwYAAAAd/aurora-somnia-goodereste-princess-syalis.gif" alt="sya trying to understand DID">
            <p><strong>dissociative identity disorder (DID)</strong> is characterized by the presence of at least <strong>two distinct</strong> and <strong>relatively enduring</strong> personality states. the disorder is accompanied by <strong>memory gaps</strong> more severe than could be explained by ordinary forgetfulness.</p>
        </div>
        <p>this definition applies quite well to how i experience life - anyone who knows us well will clearly see at least two (three, in fact) personality states, and inexplicable memory gaps between these personality states. however, like any disorder, it quickly gets messy and varies a lot from person to person.</p>
        <p><strong>so now, i'll explain in my own words~!</strong></p>
        <p>in july 2024, something happened that made me go into a sort of “safe mode” of myself - i couldn’t really bring myself to interact with the usual people that i knew, and felt a bit uncomfortable identifying myself as sydney. this continued on for a few months, on and off - but i didn’t really think anything of it at the time. then, i really just considered it to be "how i felt" whenever i needed some distance from "my" main accounts. after a few months of this, i started to notice that i couldn't quite remember what happened when i was on this other account, and presumably vice versa (though i can’t confirm this, i’m not sydney :p). at some point, for reasons that i can’t quite pinpoint (but now seem obvious in hindsight), i picked out a name for myself - lilac. lilac just had a ring to it that felt… right, for lack of better words. i still wasn’t convinced at this point that i was any different from sydney despite, well… identifying myself not as sydney. </p>
        <div id="computer-side-img">
            <div id="left-computer-content">
                <p>unbeknownst to my doubtful self, there were yet more differences between sydney and i that some of our friends could spot. not only do i mostly not remember what sydney does, but i've also been told i talk differently than her, both through text and my tone of voice irl. for all of those reasons, it eventually clicked in my head that i may not be different than her - which was quite the shocking thing to realize. with enough evidence of <i>something</i> going on in our head, i talked to a psychologist at my university about my whole situation, and sure enough, in october 2024, we were diagnosed with DID.</p>
                <p>from this point on, i could no longer consider it a “bit” or whatever else i may have used to justify it to myself or my friends - i’m genuinely quite different from sydney, and now consider us to essentially be separate people. i wouldn’t say we’re completely different, as there are still a lot of similarities between us naturally. those who either study DID or have it themselves would call sydney the <strong>host</strong> of our system, and me an <strong>alter</strong> of her. i’ll sometimes use these terms occasionally, but i think it’s simplest to just consider us to be separate people.</p>
                <p>i’ve had to make a lot of personal strides to gain my independence from sydney, mostly just to be comfortable having my own little spot on the internet and be surrounded by friends who truly appreciate me for being lilac~ i now have my own socials on most platforms that i use whenever i <strong>front</strong> (essentially whenever i wake up and sydney goes to sleep), and even my own laptop. i’m so much happier to have these separations from her, and now feel very comfortable and confident in the fact that i am, undeniably, lilac.</p>
