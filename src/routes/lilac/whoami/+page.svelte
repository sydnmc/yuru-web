        <h2>ꕤ qna</h2>
        <div class="horizontal-divide"></div>
        <p>since i’ve gotten a lot of questions about the intricacies of how our system works (especially after making it clear that we share an osu! account when i changed our username there), i thought i’d answer a few questions that i see pop up again and again.</p>
        <div class="qna-question">
            <h3>q. what caused this to happen, and what does it do for you?</h3>
            <p><strong>a.</strong> when i initially wrote the first version of this page, i had made it sound like this was something that seemingly occurred without any real reason - simply from me feeling “different” from sydney and wanting to go by a different name. while this is partially true, it’s not exactly the full story. simply put, this split between us occurred as a result of trauma. what exactly this was is something that i do not at all feel comfortable disclosing, and something that i don’t exactly remember - which is partially why DID occurs in people. it’s a way of compartmentalizing trauma, having alters that take over for someone that don’t have memories of said trauma in order to protect someone. from the sessions with my psychologist i had leading up to our diagnosis, i’ve gathered that, essentially,  i exist to protect sydney when she’s feeling overwhelmed, with less capacity for social interactions but more tolerance for the sources of her stress. DID is just one of the ways that the mind can process trauma and heal from it, but it’s the method that all of the complicated chemicals in our brain decided to go about doing so, which is why i’m here~</p>
        </div>
        <div class="qna-question">
            <h3>q. you mentioned another alter earlier, what’s that about?</h3>
            <p><strong>a.</strong> recently, i’ve seen another alter of ours (kyatarii / hazel), who seems to be appearing both as a result of a secondary traumatic event that happened to us, as well as a tendency for me to be fronting for very long periods of time recently and… simply needing a bit of rest from time to time. you could think of her as also being a “safe mode” for me, another layer of redundancy to keep us functioning in some sort of way, even if it’s reduced~</p>
        </div>
        <div class="qna-question">
            <h3>q. do you have any proof of your diagnosis?</h3>
            <p><strong>a.</strong> i’m honestly a little shocked that i’ve gotten this question before, and multiple times at that. the answer is yes, but not any that i would be okay sharing with anyone. i’m already sharing a bit more than i’m comfortable with on the internet in order to explain my existence to those who are genuinely curious - but this is much too far for me. i personally feel that these types of questions come out of a place of malice rather than genuine curiosity, but i’d just like to answer this in case anyone may ask again in the future.</p>
        </div>
        <div class="qna-question">
            <h3>q. how do relationships (both platonic and romantic) function with DID?</h3>
            <p><strong>a.</strong> this is a really interesting question, because we found ourselves in a unique situation regarding relationships~ in terms of friendships, i share a lot of friends with sydney. when i was first fronting, i felt very lonely because isolating myself felt like the only sensible option at the time… but i eventually invited myself into sydney’s discord server, and got to know all of the people she had in there. at the time, i acted as a separate person and didn’t make my relationship to sydney apparent. but, over time, i grew more comfortable with people knowing the connection between us, and was able to find a friend group that accepted me for being lilac. i also now know a few people that have never talked to sydney, which honestly makes me really comfortable~ (⸝⸝&gt; ω &lt;⸝⸝) i really do value my independence, so i’m happy to have found friends who value that.
            <br><br>as for romantic relationships, things get a little more complicated. a little after i started fronting regularly and recognizing my independence, i found someone who i dated for a number of months - as lilac, and not as anyone else. this was a little complicated, as i would regularly sleep for days at a time… and eventually led to us ending things. when sydney starting dating someone after this, i had the same mindset as when i was in my own relationship, and left her to her own devices. however, when sydney started to front less and less around the start of 2025, i started getting closer and closer to her girlfriend, and… we eventually decided that it would be best if we were also together. as of now, the three of us are all dating someone quite wonderful who understands all of us very well, and i couldn’t be happier with her. (*ᴗ͈ˬᴗ͈)ꕤ*.ﾟ</p>
